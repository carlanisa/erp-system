$updated = 0; $skipped = 0; $failed = 0; $conflicts_cleared = 0;

foreach ($map as $name => [$code, $brand, $category]) {
    // Find the ACTIVE product by name
    $product = DB::table('products')
        ->whereNull('deleted_at')
        ->where('is_active', true)
        ->where('name', $name)
        ->first(['id', 'sku', 'name']);

    if (!$product) {
        // Try archived product too
        $product = DB::table('products')
            ->whereNull('deleted_at')
            ->where('name', $name)
            ->orderBy('id', 'desc')
            ->first(['id', 'sku', 'name']);
    }

    if (!$product) {
        $skipped++;
        continue;
    }

    // If SKU already correct, skip
    if ($product->sku === $code) {
        $skipped++;
        continue;
    }

    // Rename ALL OTHER products (active/archived/deleted) that have this SKU
    $cleared = DB::table('products')
        ->where('sku', $code)
        ->where('id', '!=', $product->id)
        ->update(['sku' => 'OLD-' . $code, 'updated_at' => now()]);

    if ($cleared > 0) {
        $conflicts_cleared += $cleared;
    }

    // Now update the target product with the real CODE
    $rows = DB::table('products')
        ->where('id', $product->id)
        ->update([
            'sku'        => $code,
            'brand'      => $brand ?: null,
            'category'   => $category ?: null,
            'is_active'  => true,
            'status'     => 'active',
            'updated_at' => now(),
        ]);

    if ($rows > 0) {
        $updated++;
        if ($updated % 50 === 0) {
            echo "  [{$updated}] products updated...\n";
            flush();
        }
    } else {
        $failed++;
        echo "  ❌ FAILED: ID={$product->id} Name={$name}\n";
    }
}

